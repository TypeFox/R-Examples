dicweib <- function(x,shape,scale,log=FALSE) {
  if (is.matrix(x)) {
    day1 <- x[,1]
    day2 <- x[,2]
  } else {
    day1 <- x[1]
    day2 <- x[2]
  }
  v <- log(pweibull(day2,shape,scale)-pweibull(day1,shape,scale))
  if (log) v else exp(v)
}

