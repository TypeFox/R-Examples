print.tpopt <- function(x, ...)
{
    res <- x
    cat("\n###############################################################################\n")
    cat("Models:\n")
    print(res$eta)
    cat("Fixed parameters:\n")
    print(res$theta.fix)
    cat("\n###############################################################################\n")
    cat("Design:\n")
    print(rbind(x = res$x, w = res$w))
    cat("\n###############################################################################\n")
}
